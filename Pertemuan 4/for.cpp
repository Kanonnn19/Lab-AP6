//for
#include <iostream>
using namespace std;
int main() {
    int x;

    cout << "Inputkan sebuah angka : ";
    cin >> x;

    //inisialisasi; kondisi; iterasi
    for (int a = x; a <= 10; a++){
        cout << a << endl;
    }
    //proses 1 : a-> 1 lalu dicek kondisi, 1 <= 10 benar, maka cetak 1 lalu iterasi a-> 2
}